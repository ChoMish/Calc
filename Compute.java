class Compute {
   private int a;
   private int b;
    public Compute(int a, int b) {
        this.a = a;
        this.b = b;
    }
    int plus () {
        return a + b;
    }
    int minus () {
        return  a - b;
    }
    int multiplication () {
        return a * b;
    }
    int division () {
        return a / b;
    }
}
